package com.example.crimereporter;

public class UserInformation {

    public String name, city, country, postAddress, mobileNo;

    public UserInformation(){

    }

    public UserInformation(String name, String city, String country, String postAddress, String mobileNo) {
        this.name = name;
        this.city= city;
        this.country= country;
        this.postAddress= postAddress;
        this.mobileNo= mobileNo;

    }

}
